package com.henallux.stream.example.streamexample.exceedingamount;

public class Amount {
    private String currencyAmount;
    private int minorUnitDecimals;

    public String getCurrencyAmount() {
        return currencyAmount;
    }

    public void setCurrencyAmount(String currencyAmount) {
        this.currencyAmount = currencyAmount;
    }

    public int getMinorUnitDecimals() {
        return minorUnitDecimals;
    }

    public void setMinorUnitDecimals(int minorUnitDecimals) {
        this.minorUnitDecimals = minorUnitDecimals;
    }
}
